package javaQuestions;

public class NumPositiveOrNegative {
	
	public static void main(String[] args) {
		
		double d=-1;
		
		if(d>0.0)
		{
			System.out.println("num is positive "+d);
		}
		else if(d<0.0)
		{
			System.out.println("num is negative"+d);
		}
		else
		{
			System.out.println("num is zero"+d);
		}
		
		
	}

}
